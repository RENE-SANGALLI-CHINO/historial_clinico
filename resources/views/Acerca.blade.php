        <p>
            -<span style="white-space: pre;"> </span> Somos un Hospital Público Provincial.&nbsp;<br>
        </p>
        <p align="justify">En el año 2007, el Hospital Municipal de Viacha (inicialmente llamado Hospital de segundo nivel) pasó por ley 
           al sistema público del Gobierno Nacional y se modificó el sistema de conducción que quedó conformado por un Consejo Técnico, una
           Dirección Ejecutiva y tres Direcciones con áreas de competencia delimitadas. Contar con un presupuesto anual garantizado por el
           Estado Nacional, a través del Gobierno Autónomo Municipal de Viacha, posibilita mejorar la gestión de los recursos y garantizar la
           transparencia administrativa que, junto a la construcción de nuevos espacios y la puesta en funcionamiento del edificio, buscan 
           brindar una atención de alta calidad con confort para los pacientes y mejores condiciones laborales para los trabajadores. 
           Esta definición nos atribuye un rol específico dentro del sistema de salud en el que estamos inmersos, y así, junto con otros 
           servicios asistenciales nacionales, contribuimos a la consolidación del modelo nacional de salud y aportamos desde el área de la 
           salud a la construcción de una sociedad más justa y democrática.
        </p>
        <p>
            -<span style="white-space:pre"> </span> Trabajamos en sistema de red.&nbsp;
        </p>
        <p align="justify">Trabajar en redes integradas de salud implica promover acuerdos con otras organizaciones para prestar servicios de 
           salud equitativos e integrales para la población a la que servimos. Es lograr la articulación adecuada con otras instituciones 
           optimizando los mecanismos de referencia y contra referencia para disponer así de todos los niveles de atención y proveer servicios 
           electivos y de urgencia, cuidados agudos, de largo plazo, paliativos y de rehabilitación. El Hospital es esencial para la articulación 
           de la atención integrada y continua en la prestación de servicios de salud integrales. La resolución de problemas de salud trasciende 
           la solución de problemas médicos.&nbsp;
        </p>
        <p>
            -<span style="white-space:pre">	</span> Disponemos de la mayor complejidad posible.&nbsp;
        </p>
        <p align="justify">Entendemos a la mayor complejidad como la capacidad de dar respuesta oportuna a personas con problemas de salud 
           complejos que requieren capital humano, tecnológico y edilicio de alta especificidad para dar dicha respuesta. Disponer de la mayor
           complejidad será diferente en cada área y servicio y quedará supeditado a los estándares definidos y a las necesidades de la 
           población que demande los servicios del Hospital.
        </p>
        <p>
            -<span style="white-space:pre"> </span> Nos dedicamos al cuidado integral y humanizado de la población en todas las etapas de la vida.
        </p>
        <p align="justify">Brindar cuidado integral y humanizado es proporcionar atención a individuos, familias y comunidad de forma resolutiva,
           oportuna y eficaz, con un enfoque intersectorial y con equipos interdisciplinarios capacitados que basan su acción en una mirada
           integradora del ser humano. Este abordaje permite pensar al ser humano como un ser biopsicosocial inmerso en entramados familiares y 
           culturales, con una historia individual y social. El cuidado integral y humanizado recupera una mirada dinámica del proceso de salud e 
           introduce en el proceso salud-enfermedad-atención la perspectiva de la persona y su familia en la vivencia del proceso. Rescata así la
           empatía y el respeto como bases del vínculo humano establecido entre el equipo de salud, los pacientes y sus familias. Humanizar la 
           atención es recordar que en la relación entre el equipo de salud y las personas enfermas hay seres humanos que ponen en juego vínculos, 
           emociones, creencias y significados personales.
        </p>
        <p>
            -<span style="white-space:pre">	</span> En el marco de los derechos de las personas.
        </p>
        <p align="justify">Desde esta concepción la salud es un derecho humano básico. Pensar la salud como un derecho social es pensarla como
           la capacidad permanente de los individuos y las comunidades de modificar los aspectos y las condiciones que obstaculizan el desarrollo 
           de una vida plena. Es la construcción colectiva de un proyecto de vida digno. En la actividad hospitalaria, esto implica asegurar a la 
           población beneficiaria el derecho a recibir y demandar una atención personalizada, digna, de calidad y con base en los principios de la
           bioética centrado en la persona como sujeto de derechos.
        </p>
        <p>
            -<span style="white-space:pre">	</span> Debemos garantizar accesibilidad.&nbsp;
        </p>
        <p align="justify">La accesibilidad es un derecho. Es el grado en el que las personas pueden usar un objeto, visitar un espacio o acceder
           a un servicio, más allá de sus capacidades técnicas, cognitivas o físicas. Garantizar el acceso es tener en cuenta las barreras 
           organizacionales, económicas, culturales, físicas y emocionales para proveer de manera eficiente los servicios brindados.
        </p>
        <p>
            -<span style="white-space:pre">	</span> Equidad.&nbsp;
        </p>
        <p align="justify">Garantizar la equidad es evitar que las diferencias individuales, sociales y culturales se reflejen en la oportunidad 
           de obtener atención sanitaria digna y de calidad. Promover la equidad es evitar las desigualdades injustas. Nuestro rol es garantizar 
           la atención de los que más dificultades tienen para conseguirla.
        </p>
        <p>
            -<span style="white-space:pre">	</span> Atención segura.&nbsp;
        </p>
        <p align="justify">La atención segura es evitar el daño involuntario que pueden sufrir las personas durante la atención sanitaria. Implica 
           disminuir los eventos adversos, prevenir errores y minimizar riesgos evitables.
        </p>
        <p>
            -<span style="white-space:pre">	</span> Calidad en la asistencia, investigación, formación y cuidado del capital humano.
        </p>
        <p align="justify">Este concepto incluye el mejoramiento de los servicios de salud en todos sus aspectos, las prácticas, la infraestructura,
           el equipamiento y la capacidad profesional de todo el equipo de salud. En la atención, calidad se refiere al cumplimiento de estándares 
           de estructuras, procesos y resultados predeterminados, destinados a asegurar la eficacia, la efectividad y la eficiencia en la resolución 
           de los problemas de salud. La calidad del capital humano refiere al grado de integración y la capacidad de superar oposiciones personales 
           sobre la base de principios, valores éticos y códigos de convivencia que mantienen el tejido social y satisfacen las necesidades 
           institucionales y de los sistemas de salud locales, regionales, provinciales y de la política pública. Para el equipo de salud del 
           Hospital, la calidad en formación implica el diseño, la puesta en marcha y el monitoreo de un plan estratégico de capacitación 
           construido participativamente bajo criterios de eficacia, eficiencia y efectividad. Se aspira a que todas las actividades de 
           capacitación realizadas puedan generar las competencias laborales necesarias para alcanzar los objetivos y metas institucionales.&nbsp;
        </p>
        <p align="justify">La investigación de calidad debe ser relevante para resolver problemas del proceso salud-enfermedad-atención de los 
           pacientes y la comunidad, con observancia de los principios éticos establecidos y cumpliendo con criterios de rigurosidad metodológica
           y con estándares suficientes como para ser comunicados en revistas indexadas y que adhieran a los principios éticos establecidos. 
           Conceptualmente la investigación debe ser asumida por la comunidad hospitalaria como destinada a mejorar la asistencia, siendo además 
           importante promocionar el empleo de los resultados de las investigaciones en la práctica institucional. Se incorpora el paradigma de 
           la salud ocupacional para el cuidado del capital humano y la prevención de riesgos.
        </p>
        <p>
            -<span style="white-space:pre">	</span> Queremos ser… un hospital referente, generador de modelos, innovador…&nbsp;
        </p>
        <p align="justify">Los factores por los que el Hospital ha sido reconocido y ha logrado su prestigio son la formación de capital humano 
           y la capacidad de recibir pacientes de toda la provincia Ingavi y municipios aledaños. Algunas áreas han completado desarrollos 
           innovadores y de gran importancia. Queremos universalizar ese desarrollo para que todos los servicios del Hospital sean innovadores 
           y que los desarrollos del Hospital puedan ser replicados al exterior de la Institución y a lo largo de todo el departamento de La Paz.
        </p>
        <p>
            <b>CONSIDERACIONES ÉTICAS</b>&nbsp;
        </p>
        <p align="justify">Como organización sanitaria tenemos que articular la ética individual con la ética organizacional, y velar por que ésta 
           sea una ética de la responsabilidad y del interés común. La ética de la organización es la articulación, aplicación y evaluación de 
           los valores relevantes y posicionamientos morales de una organización, que la definen tanto interna como externamente.&nbsp;
        </p>
        <p align="justify">En la relación hospital/ paciente se propone adoptar el modelo contractual-moral, asumiendo la responsabilidad social 
           que al hospital y a su personal competen: un modelo en el que los involucrados o afectados directa o indirectamente en sus intereses 
           (el equipo de salud en conjunto con el paciente y su entorno) asumen un contrato legal pero también un contrato moral cuya prioridad es 
           la atención del paciente.&nbsp;
        </p>
        <p align="justify">Tener que situar al paciente, a sus familias y a los agentes de salud en el marco de una estructura humana, física y
           funcional donde se produce la interacción (el Hospital) genera la necesidad de regirse por principios y valores que guíen esa 
           interacción y contribuyan a la mejor relación con el paciente. Esos principios y valores serán relevantes en la actividad de la 
           institución y sus procesos de toma de decisiones guardarán coherencia con los mismos. En consonancia con su utilización en la 
           bioética clínica, también la ética de las organizaciones se rige por los principios de no maleficencia, justicia, autonomía y 
           beneficencia.&nbsp;
        </p>
        <p align="justify">La adopción y articulación de los valores, así como su aplicación práctica, se fundamenta en la deliberación y el 
           consenso. En el equipo de salud hay que promover el desarrollo de una cultura que reconozca y valorice su responsabilidad frente al 
           paciente y su entorno social, y que considere prioritaria la respuesta de la institución a sus necesidades e intereses.&nbsp;
        </p>
        <p align="justify">Se aspira a que el Hospital viva un clima ético que incluya al conjunto de sus componentes. El clima ético consiste 
           en la percepción que tienen todos los miembros de la institución de que, cuando se toman las decisiones, se está decidiendo en base 
           a esos valores acordados.&nbsp;
        </p>
        <p align="justify">A fin de alcanzar el clima ético deseable en el Hospital es necesario articular tres tipos de éticas: a) una ética 
           clínica interdisciplinaria, es decir, una ética del cuidado del paciente; b) una ética de la gestión y la organización, con 
           eficiencia pero también con responsabilidad social; y c) una ética profesional, de los trabajadores de la salud, que incluye el 
           ejercicio responsable de sus propios derechos en el marco del respeto a los derechos de los pacientes.&nbsp;
        </p>
