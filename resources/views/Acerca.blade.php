        <p>
            <b>A. MODELO DE ATENCION&nbsp;</b>
        </p>
        <p>Las metas de la medicina aportan sentido y legitimidad social a la actividad sanitaria. Estas son:</p>
        
        <p align="justify">1. Prevenir la enfermedad y las lesiones. Promover y mantener la salud.&nbsp;</p>
        <p align="justify">2. Aliviar el dolor y el sufrimiento causados por la enfermedad.&nbsp;</p>
        <p align="justify">3. Asistir, curar y rehabilitar a los enfermos; y cuidar a los que no pueden ser curados.&nbsp;</p>
        <p align="justify">4. Evitar la muerte prematura, y velar por una muerte en paz.</p>
        
        <p align="justify">Para la atención eficiente, eficaz y oportuna de la población en todas las etapas de la vida el modelo de atención
           deseado para el Hospital Posadas asume que el individuo, su familia y comunidad son el centro de la atención. Este modelo se basa en 
           el cuidado integral y humanizado de las personas consideradas en su integralidad en una concepción biopsicosocial de la atención de la 
           salud poniendo el acento en valores como el acceso, la equidad, la seguridad en la atención y la calidad en la asistencia.&nbsp;
        </p>
        <p align="justify">Para el desarrollo de este componente se plantearon objetivos y actividades y se definieron metas a cumplir para las 
           cuales se establecieron algunos indicadores de medición.&nbsp;</p><p>Se detallan a continuación los objetivos con sus respectivas 
           actividades. Los indicadores y las metas están detallados en el cuadro resumen al final del documento:
        </p>
        
        <p align="justify">1. Instalar al paciente como sujeto de atención&nbsp;</p>
        <p align="justify">Para esto es necesario empoderar al paciente y evitar que sea destinatario pasivo de nuestras prácticas. Son acciones 
           fundamentales para cumplir con este objetivo y así poder implementar un modelo de atención con las características propuestas.&nbsp;
        </p>
        <p align="justify">El derecho de los pacientes a recibir y demandar una atención personalizada, digna, de calidad y con base en los 
           principios de la bioética debe ser conocido por toda la comunidad hospitalaria. El conocimiento de estos derechos por todo el equipo de 
           salud permitirá garantizar la participación de todos como sujetos de derechos.
        </p>
       
        <p>Actividades:</p>
        <p align="justify">-<span style="white-space:pre">	</span>1. Elaborar e implementar un dispositivo para que el personal conozca y 
           practique los principios éticos de conducta en la atención de personas.&nbsp;
        </p>
        <p align="justify">-<span style="white-space:pre">	</span> 2. Diseñar y poner en funcionamiento un dispositivo para que los pacientes 
           conozcan y ejerzan sus derechos y responsabilidades.</p><p>&nbsp;</p><p>Los dispositivos propuestos permitirán concientizar y 
           sensibilizar a toda la comunidad hospitalaria favoreciendo el trato integral y humanizado. Incluye, entre otros, espacios de reflexión y 
           articulación entre el equipo de salud y la comunidad, actividades de capacitación, recursos de comunicación social y evaluación, etc.
        </p>
        <p align="justify">Al mismo tiempo, es necesario recordar y fortalecer constantemente las acciones que hacen al buen trato y al respeto.&nbsp;
        </p>
        <p align="justify">-<span style="white-space:pre">	</span>3.&nbsp; Generar instancias de representación de los pacientes para la toma de 
           decisiones.&nbsp;</p><p>Existen ya grupos de pacientes que se agrupan y exigen una atención digna y de calidad a partir de sus 
           necesidades particulares y en función de las patologías que los aquejan (por ejemplo, el grupo de pacientes con artritis reumatoidea). 
           Empoderar al paciente y volver a darle voz para que se convierta en actor activo de su propia salud es el primer paso en un largo camino 
           que se completará con la participación de estos pacientes en las instancias de planificación y decisión. Para comenzar, es importante 
           recuperar e instalar el concepto de satisfacción del usuario y sistematizar las encuestas de opinión como herramientas para escuchar a los 
           pacientes.
        </p>
        <p>&nbsp;</p>
        <p align="justify">2. Mejorar la calidad de atención&nbsp;</p>
        <p align="justify">Basar las decisiones en los fundamentos de la epidemiología, la evidencia y la planificación; analizar críticamente
           lo que hacemos para compararlo con lo que debemos hacer. Esto permitirá terminar con el personalismo, que hasta hace poco fue el motor 
           del crecimiento (o del estancamiento) en el desarrollo de los Servicios que dependían de capacidades de intervención y negociación 
           individuales.&nbsp;
        </p>
       
        <p>
