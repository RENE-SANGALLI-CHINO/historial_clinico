            <b>Visión</b><br>
        </p>
        <p align="justify">“Viacheños y viacheñas ejercen plenamente su derecho a la salud en igualdad de condiciones, con un sistema de salud
           acreditado con mejor infraestructura, equipamiento y RRHH, que le permite brindar atención universal gratuita con calidad, calidez y
           prontitud mediante un Sistema Único de Salud Familiar Comunitaria Intercultural”.<br>
        </p>
        
        <p>
            <b>Misión</b>
        </p>
        <p align="justify">“La red Municipal de salud, en sus 10 establecimientos, brinda servicios de salud públicos, con calidad y calidez, 
           oportunos y basados en la interculturalidad e integralidad, buscando la satisfacción del usuario, de la sociedad civil y del personal
           de salud”<br>
        </p>
        
        <p>
            <b>Objetivo General</b>
        </p>
        <p align="justify">Fortalecer el servicio de atención de los centros de salud y hospital municipal, para satisfacer las necesidades de 
           salud de la población, con la contratación de personal médico y administrativo, idóneo, para cada establecimiento en salud y brindar 
           mejor calidad en servicio de salud, realizar campañas de salud para prevenir enfermedades mortales a la población.<br>
